import math

# in this exercise, we will see some functions mathematics functions.
num = 3.9

# round(): it will round up the number to nearest whole number.
print(round(num))

# abs(), which is absolute function, it will change negative number to the positive.
print(abs(-3.9)) # 3.9

# ceiling() method, it will change the number to nearest whole number.
print(math.ceil(2.9)) # 3

# floor() method, it will change the number to nearest small whole number.
print(math.floor(5.8)) # 5


